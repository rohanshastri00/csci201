package objects;

public class Time {
	private String start;
	private String end;

	public String getStartTime() {
		return start;
	}

	public String getEndTime() {
		return end;
	}

	public void setStart(String start) {
		this.start = start;
	}

	public void setEnd(String end) {
		this.end = end;
	}
}
